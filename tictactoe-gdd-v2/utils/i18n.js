import { useState, useEffect } from 'react';

let _lang = 'en';
let _listeners = [];

export const setLanguage = (code) => {
  if (!T[code]) code = 'en';
  _lang = code;
  _listeners.forEach(fn => { try { fn(code); } catch(e){} });
};
export const getCurrentLang = () => _lang;

export const useLang = () => {
  const [lang, setLangState] = useState(_lang);
  useEffect(() => {
    const h = (c) => setLangState(c);
    _listeners.push(h);
    return () => { _listeners = _listeners.filter(f => f !== h); };
  }, []);
  return lang;
};

export const t = (key, vars = {}) => {
  const str = T[_lang]?.[key] ?? T['en']?.[key] ?? key;
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)), str
  );
};

export const detectDeviceLanguage = () => {
  try {
    const code = (Intl.DateTimeFormat().resolvedOptions().locale || 'en').split('-')[0].toLowerCase();
    return T[code] ? code : 'en';
  } catch { return 'en'; }
};

